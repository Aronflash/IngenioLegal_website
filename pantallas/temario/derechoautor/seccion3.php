                  <h2 class="heading-decorate"><span class="divider"></span>Sanciones y Multas</h2>
                  <p class="p big text-gray-800">Esta Ley impone sanciones severas para proteger los derechos de los autores y asegurar el respeto a sus obras. Estas sanciones incluyen multas y penas de prisión para quienes infrinjan la ley.</p>
                  <p class="p"> Según el artículo 109, el titular de los derechos de explotación de una obra puede solicitar al juez la declaración de sus derechos y la prohibición de cualquier violación. En caso de incumplimiento, el juez puede imponer una multa que puede llegar hasta veinte veces el salario mínimo urbano vigente, con la posibilidad de convertirla en arresto proporcional. En caso de reincidencia, esta multa puede duplicarse.</p>
