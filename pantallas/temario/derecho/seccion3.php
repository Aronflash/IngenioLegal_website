                  <p class="p big text-gray-800">Derechos Objetivos: Estos se refieren a las normas, leyes u obligaciones que deben cumplirse para regular la conducta humana. Su propósito es establecer límites y guiar el comportamiento de las personas en la sociedad. Los derechos objetivos pueden ser activos, que implican una acción a realizar (como el pago de impuestos), o pasivos, que son acciones a evitar (como la prohibición de matar). En esencia, los derechos objetivos dictan lo que es permitido o prohibido en la sociedad.</p>
              
