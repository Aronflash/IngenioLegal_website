                <h2>Las normas jurídicas</h2>
                <div class="divider-lg"></div>
                <p class="p big text-gray-800">Las normas judiciales, o normas jurídicas, son reglas de conducta establecidas y sancionadas por el Estado para regular las relaciones entre personas e instituciones dentro de una sociedad. Estas normas están codificadas en leyes, reglamentos y otras disposiciones legales, y su cumplimiento es obligatorio. A diferencia de las normas morales, las normas jurídicas tienen un carácter coercitivo, lo que significa que el Estado puede imponer sanciones a quienes no las cumplan.</p>

                <p class="p big text-gray-800">Una de las características principales de las normas jurídicas es su exterioridad, ya que se aplican de manera externa, independientemente de las creencias o conciencia personal del individuo. Lo esencial es el comportamiento observable y su conformidad con la ley. Además, estas normas son heterónomas, lo que significa que son impuestas por una autoridad externa (el Estado), y su cumplimiento no depende de la voluntad individual; las personas están obligadas a acatarlas, estén o no de acuerdo con ellas.</p>
