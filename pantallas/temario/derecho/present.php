                <h2>Las normas morales</h2>
                <div class="divider-lg"></div>
                <p class="p big text-gray-800">Las normas morales son directrices que orientan el comportamiento humano basadas en principios éticos y valores de lo que se considera correcto o incorrecto. A diferencia de las leyes formales, estas normas no están escritas ni codificadas, sino que emergen de la conciencia individual y de la cultura social. Se forman a partir de la educación, las tradiciones, la religión y las experiencias personales, y pueden variar entre diferentes sociedades e individuos.</p>

